// Print.cpp
// Implementation of the print_Hello function
#include <iostream>                // Include the necessary header for input/output streams
#include "../include/Print.hpp"    // Include the declaration of the print_Hello function

/**
 * @brief Prints "helloWorld" to the standard output.
 * 
 * This function outputs the string "helloWorld" followed by a newline to
 * the standard output stream using the std::cout stream.
 */
void print_Hello(void)
{
    std::cout << "helloWorld" << std::endl;    // Output "helloWorld" followed by a newline
}
